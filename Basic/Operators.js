// Comparison Operator
console.log("----------Comparison Operators----------");

console.log(x>y);
console.log(x<y);
console.log(x<=y);
console.log(x>=y);


// Relational Operator

console.log("-------Relational Operator-----------");

x = 20;
y = 30;

console.log(x<y && y>x);
console.log(x>y || x<y);

// Bitwise Operator

console.log("----- BITWISE OPERATOR -------");

console.log(5 & 7);
console.log(5 | 7);
console.log(5 ^ 7);
console.log(~7);

// Equality Operators

console.log("----- EQUALITY OPERATORS ----");

let a = 10;
