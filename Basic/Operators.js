// Arithmatic Operator

let x = 10;
let y = 20;

console.log(x + y); //addition
console.log(x - y); // subs
console.log(x * y); //mult
console.log(x / y); // div
console.log(x % y); //rem
console.log(x ** y); // expo

// ASSIGNMENT OPERATOR

